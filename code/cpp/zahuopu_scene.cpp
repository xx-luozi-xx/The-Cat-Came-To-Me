static void sceneReady(scene& scn, player& p);
static void sceneEnd(scene& scn);
void zahuopu(player& p) {
	scene zahuopu;
	sceneReady(zahuopu, p);
	zahuopu.gameStart();
    //动画
    try {
        while (1) {
            zahuopu.printMap();
 
            zahuopu.playerDo();//其他按键功能
            zahuopu.NPCDo();
            zahuopu.playerMove();
            zahuopu.NPCMove();
            zahuopu.esc();

            Sleep(50);
        }
    }
    catch (int) {//换地图，地图文件存档

    }
    catch (const char*) {//退出
        p.exit = 1;
    }
    sceneEnd(zahuopu);
}

