static void sceneReady(scene& scn, player& p);
static void sceneEnd(scene& scn);
void tushuguan(player& p) {
	scene tushuguan;
	sceneReady(tushuguan, p);
	tushuguan.gameStart();
    //动画
    try {
        while (1) {
            tushuguan.printMap();
 
            tushuguan.playerDo();//其他按键功能
            tushuguan.NPCDo();
            tushuguan.playerMove();
            tushuguan.NPCMove();
            tushuguan.esc();

            Sleep(50);
        }
    }
    catch (int) {//换地图，地图文件存档

    }
    catch (const char*) {//退出
        p.exit = 1;
    }
    sceneEnd(tushuguan);
}

